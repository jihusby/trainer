package no.husby.trainer;

public class Athlete {
	
	public String name;
	public int height;
	
	public Athlete(String name, int height) {
		super();
		this.name = name;
		this.height = height;
	}

	public String getName() {
		return name;
	}

	public int getHeight() {
		return height;
	}
	
	
	
	

}
